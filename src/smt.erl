-module(smt).

%%%----------------------------------------------------------------------------
%%% Exports
%%%----------------------------------------------------------------------------

-export([t/0]).

%%-----------------------------------------------------------------------------
t() ->
    Url = get_url(),
    Count = get_count(),
    Hz = get_hz(),
    Seconds = get_seconds(),
    error_logger:info_report({?MODULE, t, ?LINE, Url, Count, Hz, Seconds})
    .

%%-----------------------------------------------------------------------------
get_url() ->
    application:get_env(smt, url)
    .

%%-----------------------------------------------------------------------------
get_count() ->
    application:get_env(smt, count)
    .

%%-----------------------------------------------------------------------------
get_hz() ->
    application:get_env(smt, hz)
    .

%%-----------------------------------------------------------------------------
get_seconds() ->
    application:get_env(smt, seconds)
    .

%%-----------------------------------------------------------------------------
